#include "libft.h"

char			**ft_sstrnew(int len, int w)
{
	char	**s;
	int		i;

	i = 0;
	s = malloc(sizeof(char *) * (len + 1));
	while (i < len)
		s[i++] = ft_strnew(w);
	s[i] = 0;
	return (s);
}
