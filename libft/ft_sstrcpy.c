#include "libft.h"

char			**ft_sstrcpy(char **dest, char **src)
{
	int i;

	i = -1;
	while (src[++i])
		dest[i] = ft_strcpy(dest[i], src[i]);
	dest[i] = 0;
	return (dest);
}
