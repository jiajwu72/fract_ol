#include "libft.h"

void			ft_print_tt_int(int **s)
{
	int i;
	int j;

	i = 0;
	while (s[i])
	{
		j = 0;
		while (s[i][j])
		{
			ft_putnbr(s[i][j]);
			ft_putstr(" ");
			j++;
		}
		ft_putendl("");
		i++;
	}
}
